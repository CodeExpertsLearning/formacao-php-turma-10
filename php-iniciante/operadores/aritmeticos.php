<?php 

/*
    +: adição
    -: subtração
    *: multiplicação
    /: divisão
    %: resto divisão
*/
$numero = 20;
$numero2 = 30;

print $numero + $numero2;
print '<hr>';

print $numero - $numero2;
print '<hr>';

print $numero * $numero2;
print '<hr>';

print $numero / $numero2;
print '<hr>';

print $numero % $numero2;
print '<hr>';